import random

def add_random_numbers():
    num1 = random.randint(1, 100)
    num2 = random.randint(1, 100)
    sum = num1 + num2
    print(f"First number: {num1}")
    print(f"Second number: {num2}")
    print(f"Sum: {sum}")

def multiply_random_numbers():
    num1 = random.randint(1, 100)
    num2 = random.randint(1, 100)
    product = num1 * num2
    print(f"First number: {num1}")
    print(f"Second number: {num2}")
    print(f"Product: {product}")


